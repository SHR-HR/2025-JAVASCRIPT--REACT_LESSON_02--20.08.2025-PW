// Импорт библиотеки React для работы с JSX
import React from "react";
// Импорт модульных стилей из файла List.module.scss
import s from "./List.module.scss";
// Импорт компонента кнопки из папки UI компонентов
import Button from "../../UI/Button/Button";

// Объявление функционального компонента ListItem (Элемент списка)
// Компонент принимает два пропса (props) через деструктуризацию:
// - item: объект элемента списка с данными {id, title, text}
// - onDelete: опциональная функция-обработчик удаления элемента
function ListItem({ item, onDelete }) {
  // Возвращаем JSX разметку элемента списка
  return (
    // Элемент списка (li) с применением стилей из модуля
    <li className={s.item}>
      
      {/* Заголовок элемента списка четвертого уровня */}
      <h4>{item.title}</h4>
      
      {/* Текстовое содержимое элемента списка */}
      <p>{item.text}</p>
      
      {/* Контейнер для кнопок действий с применением стилей из модуля */}
      <div className={s.actions}>
        
        {/* Кнопка удаления элемента */}
        <Button 
          border // Пропс для отображения кнопки с границей
          onClick={() => onDelete?.(item.id)} // Обработчик клика с опциональным вызовом
        >
          Удалить {/* Текст кнопки */}
        </Button>
        
      </div>
    </li>
  );
}

// Экспорт компонента по умолчанию для использования в других частях приложения
export default ListItem;



