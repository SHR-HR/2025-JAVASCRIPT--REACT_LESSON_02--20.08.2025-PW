// Импорт библиотеки React для работы с JSX
import React from 'react'
// Импорт компонента ListItem (Элемент списка) из текущей директории
import ListItem from './ListItem'
// Импорт модульных стилей из файла List.module.scss
import s from './List.module.scss'

// Объявление функционального компонента List (Список)
// Компонент принимает два пропса (props) через деструктуризацию:
// - items: массив элементов для отображения в списке
// - onDelete: функция-обработчик удаления элемента
function List({ items, onDelete }) {
  // Условный рендеринг: если массив items пустой (длина равна 0)
  // Возвращаем сообщение о пустом списке
  if (!items.length) return <p className={s.empty}>Список пуст — добавьте первый элемент</p>

  // Основной рендеринг компонента когда список не пустой
  return (
    // Корневой div контейнер списка
    <div>
      {/* Неупорядоченный список (ul) с применением стилей из модуля */}
      <ul className={s.list}>
        {/* Преобразование массива items в массив JSX элементов с помощью map */}
        {items.map((item) => (
          // Для каждого элемента массива создаем компонент ListItem
          // key={item.id} - уникальный ключ необходим для оптимизации React
          // item={item} - передаем весь объект элемента в дочерний компонент
          // onDelete={onDelete} - передаем функцию удаления в дочерний компонент
          <ListItem key={item.id} item={item} onDelete={onDelete} />
        ))}
      </ul>
    </div>
  )
}

// Экспорт компонента по умолчанию для использования в других частях приложения
export default List


