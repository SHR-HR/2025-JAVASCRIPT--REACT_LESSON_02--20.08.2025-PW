// Импорт библиотеки React и хука useState для работы с состоянием компонента
import React, { useState } from 'react'
// Импорт компонента кнопки из папки UI компонентов
import Button from '../../UI/Button/Button';

// Объявление функционального компонента Clicker (Кликера)
function Clicker() {
// Объявление состояния counter (счетчик) с начальным значением 0
// setCounter - функция для обновления значения счетчика
const [counter, setCounter] = useState(0);

  // Функция increment (увеличение) для увеличения значения счетчика на 1
  const increment = () => {
    // Обновление состояния: новое значение = текущее значение + 1
    setCounter(counter + 1);
  }

  // Возвращаем JSX разметку компонента
  return (
    // Корневой div контейнер компонента
    <div>
        {/* Параграф для отображения текущего значения счетчика */}
        <p>{counter}</p>
        
        {/* Компонент кнопки с обработчиком onClick */}
        {/* При клике вызывается функция increment */}
        {/* Текст кнопки: "click me" - требует локализации на русский язык */}
        <Button onClick={increment}>click me</Button>
    </div>
  )
}

// Экспорт компонента по умолчанию для использования в других частях приложения
export default Clicker
