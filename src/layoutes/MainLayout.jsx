// Импорт библиотеки React для работы с JSX
import React from 'react'
// Импорт модульных стилей из файла Layout.module.scss
import s from './Layout.module.scss'
// Импорт компонента шапки (Header) из текущей директории
import Header from './Header'
// Импорт компонента подвала (Footer) из текущей директории
import Footer from './Footer'

// Объявление функционального компонента MainLayout (Главный Макет)
// Компонент принимает один пропс (props) через деструктуризацию:
// - children: дочерние элементы, которые будут вставлены в контентную область
function MainLayout({children}) {
  // Возвращаем JSX разметку основного макета приложения
  return (
    // Основной контейнер макета с применением стилей из модуля
    <div className={s.layout}>
        
        {/* Компонент шапки сайта - отображается в верхней части */}
        <Header/>
        
        {/* Контентная область с применением стилей из модуля */}
        {/* Внутрь этого div будут вставлены переданные дочерние элементы (children) */}
        <div className={s.content}>{children}</div>
        
        {/* Компонент подвала сайта - отображается в нижней части */}
        <Footer/>
        
    </div>
  )
}

// Экспорт компонента по умолчанию для использования в других частях приложения
export default MainLayout





